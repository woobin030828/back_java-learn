package castingTest;

public class CastingTest {
	public static void main(String[] args) {
//		정수 + 실수 = 실수
//		자동 형변환
		System.out.println(1+ 2.5);
//		정수 / 정수 = 정수
		System.out.println(1+1); // 귀요미
//		강제 형변환
//		최우선 연산자
		System.out.println(5 / (double)2);
		System.out.println((int)(5 / (double)2));
		
	}
}
